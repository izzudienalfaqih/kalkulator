var nilai1 = parseInt(prompt("Masukkan nilai 1"))
var operator = prompt ("Masukkan operator + - * /")
var nilai2 = parseInt(prompt("Masukkan nilai 2"))


if(operator == '+'){
    alert(nilai1 + nilai2)
}

else if (operator == '-'){
    alert(nilai1 - nilai2)
}

else if(operator== '*'){
    alert(nilai1*nilai2)
}

else if(operator== '/'){
    alert(nilai1/nilai2)
}